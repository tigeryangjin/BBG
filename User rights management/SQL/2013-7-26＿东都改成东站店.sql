   WHERE ru.user_id = rul.user_id
     AND sg.group_name LIKE '%Location%' || rul.loc || '%'
     AND RU.USER_ID IN ('R9010203264','R8803257921','R8606210323');
--Department大类    
INSERT INTO sec_user_group
