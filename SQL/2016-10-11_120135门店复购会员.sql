SELECT T.HYK_NO 会员卡号, COUNT(T.SLS_TRX_ID) 购物次数
  FROM RADM.BBG_RA_RFM_VIP_F T
 WHERE T.DT_WID BETWEEN 120150701000 AND 120150831000
   AND T.ORG_WID = 122
 GROUP BY T.HYK_NO
HAVING COUNT(T.SLS_TRX_ID) > 1
ORDER BY 2;

