   AND T1.LOC IN (118010, 118011, 118012)
   AND EXISTS (SELECT /*+PARALLEL(T1,20)*/
         1
          FROM RMSHIST.ITEM_LOC_SOH_HIST T2
         WHERE T1.ITEM = T2.ITEM
           AND T1.LOC = T2.LOC
           AND T2.SOH_BAK_DATE = GET_VDATE - 2
           AND T2.LOC IN (118010, 118011, 118012)
           AND T1.STOCK_ON_HAND * T1.AV_COST <>
               T2.STOCK_ON_HAND * T2.AV_COST);


